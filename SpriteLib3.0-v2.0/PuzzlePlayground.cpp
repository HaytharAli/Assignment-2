void PuzzlePlayground::KeyboardHold()
{

	//! Game movement
	//Set a vel variable and allow the player to move using A and D

	auto& player = ECS::GetComponent<PhysicsBody>(MainEntities::MainPlayer());
	auto& cam = ECS::GetComponent<Camera>(MainEntities::MainCamera());

	float speed = 50.f;
	b2Vec2 vel = b2Vec2(0.f, player.GetBody()->GetLinearVelocity().y);

	if (Input::GetKey(Key::A))
	{
		vel.x -= speed;
	}
	if (Input::GetKey(Key::D))
	{
		vel.x += speed;

	}

	//! Code to jump
	//Get the space key input
	//See if the vertical velocity is (near) zero
	//if vertical velocity is (near) zero, then let the player jump
	if (Input::GetKeyDown(Key::Space))
	{
		if (-0.01f < player.GetBody()->GetLinearVelocity().y && player.GetBody()->GetLinearVelocity().y < 0.01f) {
			vel.y = 500.f;
		}
	}

	player.GetBody()->SetLinearVelocity(vel);
}

